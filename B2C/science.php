                                    <h2 class="entry-title">
                                        怀孕三个月检查发现弓形虫感染，这孩子还能要吗？
                                    </h2>
                                    <span class="post-author">
                                    <span class="post-by"> 作者 : </span> 株洲市中心医院公众号 </span>
                                    <span class="post-separator">|</span>
                                    <span class="blog-post-date"><i class="fas fa-calendar-alt"></i>2019.8.5</span>
                                </header>
                                <div class="post-thumbnail img-full">
