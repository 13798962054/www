                                        <div class="entry-summary blog-post-description">
                                            <p>几天，怀孕三个月的李女士进行了抽血检查，昨天拿到了检查结果，其中围产几个项目的检查中弓形虫IgG阳性，弓形虫IgM阳性，看到这两个项目是阳性，她整个人都崩溃了。</p>
                                            <p>李女士通过网络查询得知，她感染上了弓形虫，弓形虫会严重损害未出生的胎儿，如果准妈妈在怀孕期间传染上弓形虫，对宝宝的影响非常恶劣。</p>
                                            <p>弓形虫可引起流产、死胎或新生儿疾病，或者出生后有眼、脑、肝脏的病变和畸形，如视网膜炎、白内障、脑内钙化、脑积水、智力障碍、黄疸和肝脾肿大。</p>
                                            <p>她和老公两人坐在沙发上发呆，脑袋一片空白，不知如何是好。她一直都不明白，家里也没养宠物，饮食也很卫生，怎么会感染上弓形虫呢？</p>
                                            <p>很多正在备孕或怀孕的妈妈们听到“弓形虫感染”总会有一种不明觉厉的感觉。确实，孕妇弓形虫感染有可能通过胎盘导致流产、早产、死胎、畸形或宝宝出生后会遗留中枢神经系统障碍。</p>
                                            <p>我们请到株洲市中心医院妇产科主任骆主任来为大家说说弓形虫的那些事儿，解除大家的“虫虫危机”！</p>
                                            <p>骆主任说，优生优育是提高人口质量的重要措施，“三前检查”显得尤为重要，即婚前、孕前、产前检查，对于降低出生缺陷、提高人口素质、确保母婴安全具有重要的意义。</p>
                                            <p>优生五项是孕前检查的必备项目，弓形虫检测只是其中一项，对孕妇怀孕前后和产前情况，对胎儿的健康以及其他疾病的诊断具有重要价值，可判断怀孕早期的原发性宫内感染是否影响胎儿发育的指征。</p>
                                            <p>但孕妇即使感染了弓形虫，胎儿也不一定100%会被感染，只是被感染的概率增大，如要确诊感染还需要通过其他检查手段。</p>
                                            <p class="red">一、优生五项检测的是哪几个项目？</p>
                                            <p>优生五项是一组病原微生物的检查项目，英文缩写为TORCH：</p>
                                            <p>T 刚地弓形虫（TOX）。</p>
                                            <p>R 风疹病毒（RV）。</p>
                                            <p>C 巨细胞病毒（CMV）。</p>
                                            <p>H 单纯疱疹病毒 （HSV）。</p>
                                            <p>O 代表其他病原微生物（Others），如肝炎病毒（尤其是乙型肝炎病毒）、EB 病毒、微小病毒 B19、HIV 病毒、梅毒螺旋体等。</p>
                                            <p class="red">二、化验单上的阳性该怎么办</p>
                                            <p>我们在化验单上经常看到的 IgM 与 IgG 具体是什么意思呢？骆主任介绍，这两项其实就是上述病毒的两种抗体，IgM 可以反映近 1～2 月的感染情况，IgG 则可代表人体对相应病毒的免疫力。</p>
                                            <p>检查报告单上可能会出现以下几种结果，可别看到有个（+）号整个人就慌了，了解相关知识和保持良好的心态尤为重要，不管是什么结果听从专业医生的安排。</p>
                                            <p class="mark">IgG（-），IgM（-）</p>
                                            <p>提示被检查者既往很可能没有感染过这些致病微生物，但不排除假阴性。</p>
                                            <p>如果在备孕期间检查为该结果，可以怀孕，但属于高危人群，因为备孕妇女有被感染的风险，孕早期感染传给胎儿的风险较高。</p>
                                            <p class="mark">IgG（-），IgM（+）</p>
                                            <p>提示近期感染过，或者可能为急性感染，也有可能是由于其它干扰因素造成 IgM 假阳性。</p>
                                            <p class="mark">IgG（+），IgM（-）</p>
                                            <p>表示曾经感染过这种病原微生物，或接种过该疫苗，并且已经产生免疫力。</p>
                                            <p class="mark">IgG（+），IgM（+）</p>
                                            <p>表明孕妇正在感染这种病原微生物，可能为原发性感染或再感染。</p>
                                            <p class="red">三、弓形虫通过什么途径传播？</p>
                                            <p>弓形虫通常寄养在猫、狗、鸡蛋、牛羊肉等组织中，在日常生活中，如密切与猫狗接触，经常吃火锅，吃未煮熟的肉类食品等均易感染弓形虫。</p>
                                            <p>1.如吃生或不熟的肉，经消化道传播。</p>
                                            <p>2.被动物抓破，经破损的皮肤粘膜传播。</p>
                                            <p>3.输血以及感染器官的移植。</p>
                                            <p>4.经胎盘垂直传播，当孕妇感染弓形虫后出现弓形虫血症时，弓形虫通过血流经胎盘而使胎儿出现先天性的感染。</p>
                                            <p class="red">四、弓形虫要怎么预防？</p>
                                            <p>1.不吃生肉类，吃煮熟、加工后的肉类。</p>
                                            <p>2.避免接触肉类粘膜，搬运生肉后洗手，彻底清洁炊具。</p>
                                            <p>3.吃水果和蔬菜前清洗干净。</p>
                                            <p>4.避免食物被苍蝇、蟑螂或其他昆虫污染。</p>
                                            <p>5.避免接触被猫污染过的东西（泥土、水果、蔬菜）</p>
                                            <p>6.孕期家中尽量不养猫等宠物，如养宠物在备孕前需要带宠物到宠物医院检查寄生虫情况并注射疫苗。</p>
