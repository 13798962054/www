          <p>　<strong>作者：EWA.C</strong><br>
<span style="font-size:16px;">我们机体获取传染病免疫力主要分为主动和被动两个方面，不管是主动获得还是被动获得，由于各机体的自身情况不同以及所处的环境不同，对于有些传染病的免疫保护力获得后都不是终身保护的。接种疫苗是目前预防疾病的主要手段，任何一种疫苗都不可能使100%的个体得到保护。为什么有人接种疫苗没有效果？我们该怎么评估机体的免疫保护抗体水平？<br>
&nbsp;<br>
<img alt="" src="/uploads/allimg/180724/1-1PH41T525Y8.png"><br>
来源：腾讯网<br>
&nbsp;<br>
<strong>1、&nbsp;&nbsp;&nbsp;&nbsp; </strong><strong>如何获得免疫保护性抗体</strong><br>
<img alt="" src="/uploads/allimg/180724/1-1PH41T545311.png" style="width: 811px; height: 584px;"><br>
2、如何检测免疫保护力<br>
&nbsp;<br>
通过检测血清中的特异性抗体IgG滴度是否达到世界卫生组织（WHO）规定的最低保护滴度，以及国家卫计委发布的《预防接种工作规范》上规定的免疫成功值，判断机体当前是否具备相关疾病的免疫保护能力。参考可溯源的国际标准血清数值，科学有效的评估免疫保护情况。<br>
&nbsp;<br>
3、什么是免疫保护评价<br>
&nbsp;<br>
●免疫保护评价是指通过检测现阶段机体内相关抗体水平是否达到预防疾病所需要的最低水平，从而判断当前个体免疫保护力。<br>
&nbsp;<br>
●免疫保护水平适用人群？——适用于全体人群。<br>
在园儿童、各大中小学生检测，包括成人女性，老年人，免疫功能低下者，免疫缺陷者。<br>
&nbsp;<br>
●对个人以及群体抗体水平进行监测，研究抗体水平及其几何平均浓度、阳性率及其与接种年龄的关系，以评估个人及人群免疫效果，判断感染疾病的风险，便于临床医生判断，及时采取有效措施，可避免疾病的流行发生。<br>
&nbsp;<br>
4、哪些项目可以检测</span><br>
<img alt="" src="/uploads/allimg/180724/1-1PH41TJ1110.png" style="width: 596px; height: 418px;"><br>
<span style="font-size:16px;">中南医学检验实验室“免疫保护水平监测平台”可定量监测机体免疫保护水平，可用于评估个人及人群免疫效果。评估同时，适用于流行病学调查、监测以及疫苗和接种效果的评估。<br>
&nbsp;<br>
5、中南医学免疫保护水平监测平台——产品优势<br>
&nbsp;<br>
● 维润赛润试剂产品采用自产抗原作为核心原材料，德国原装进口试剂，高品质保证；产品已获得欧盟CE认证或国家药监局注册证。<br>
&nbsp;<br>
● 定量结果，产品采用国际公认4参数逻辑对数模型(4PL)作为定量模型，可溯源国际标准品定标。<br>
&nbsp;<br>
● 严谨的实验室操作平台，方便快捷。</span><br>
