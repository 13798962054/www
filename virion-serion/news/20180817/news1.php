          <h3>关于百日咳，你真的了解吗？</h3>
          <!-- JiaThis Button BEGIN -->
          <div class="clearfix" style=" margin-top:10px; margin-bottom:10px; text-align:right;"> </div>
          <!-- JiaThis Button END -->
          <p>　</p><div style="text-align: center;">
    作者：温欣欣 周鹏</div>
<div>
    <strong>什么是百日咳？</strong><br>
    百日咳是由百日咳杆菌引起的急性呼吸道传染病。其临床特征为阵发性痉挛性咳嗽伴有深长的“鸡鸣”样吸气性吼声，如未得到及时有效的治疗，病程可迁延数个月左右，故称“百日咳”。<br>
    &nbsp;<br>
    <strong>什么是百日咳杆菌？</strong><br>
    百日咳杆菌（又称<strong>百日咳鲍特菌 </strong><strong>Bordetella pertussis</strong>）是一种小的、营养需求苛刻的革兰氏阴性球杆菌，专门附着在人呼吸道的粘膜层。<br>
    &nbsp;<br>
    <strong>百日咳杆菌</strong><strong>致病因子有哪些？</strong><br>
    百日咳毒素（PT）<br>
    丝状血凝素（FHA）<br>
    百日咳粘着素（PRN）<br>
    菌毛（FIM)2型和3型<br>
    腺苷酸环化酶毒素（ACT)<br>
    气管细胞毒素（TCT）<br>
    脂低聚糖和百日咳鲍特菌内毒素<br>
    <strong>FHA</strong><strong>、</strong><strong>PRN </strong><strong>和菌毛</strong>可帮助细菌粘附在宿主细胞上，<br>
    <strong>PT</strong><strong>、</strong><strong>TCT </strong><strong>和 </strong><strong>ACT </strong>可使细菌破坏上皮层，并躲避宿主的免疫系统，这些都是重要的致病因素。<br>
    &nbsp;<br>
    <strong>传染源：</strong><br>
    患者是唯一的传染源，非典型或轻型患者在本病的流行中起着更重要的作用。从潜伏期末1～2天，至发病后6周内都有传染性，以病初1～3周为最强。少见带菌者。<br>
    &nbsp;<br>
    <strong>传播途径：</strong><br>
    咳嗽时病原菌随飞沫传播，易感者吸人带菌的飞沫而被感染<br>
    &nbsp;<br>
    <strong>易感染的人群有哪些？</strong><br>
    &nbsp;&nbsp; 人群对百日咳普遍易感。<br>
    &nbsp;<br>
    <strong>感染</strong><strong>百日咳杆菌</strong><strong>后会出现什么症状？</strong><br>
    经9-10天（范围：6-20天）的潜伏期，患者出现卡他症状（包括咳嗽），<br>
    1-2周内，出现阵发性痉挛性咳嗽，并带有特征性的鸡鸣样尾音。<br>
    典型表现是夜间咳嗽特别严重，咳后常伴有呕吐。<br>
    <strong>不同年龄阶段的发病情况</strong><br>
    在婴幼儿中，百日咳可引发呼吸暂停和发绀，但可无咳嗽；<br>
    在青少年和成人中，非特异性的、经久不愈的咳嗽可能是唯一的临床表现；疾病的卡他期、阵发期和恢复期可持续数月；<br>
    &nbsp;<br>
    <strong>流行特征</strong><br>
    本病分布遍及全世界，多见于寒带及温带，全年均可发病。平常为散发，在幼儿园等集体机构、居住条件差的地区可发生局部流行。<br>
    我国百日咳病例一般出现在春夏季；<br>
    3月发病开始升高，<br>
    5-7月达到高峰，<br>
    8-9月开始下降，<br>
    12月最低。<br>
    但我国幅员辽阔，各地气候不同，故一年四季均有百日咳病例。<br>
    根据传染病监测资料，目前我国农村百日咳的发病率高于城市。我国每2-5年发生一次流行，每次流行持续2-3年。<br>
    &nbsp;<br>
    鉴别诊断：<br>
    <strong>可引起百日咳症状的都有哪些病原体？</strong><br>
    其它细菌如流感嗜血毒素，卡他莫拉菌，支原体肺炎，沙眼衣原体或病毒如RSV和腺病毒也可能会导致百日咳类疾病。<br>
    &nbsp;<br>
    <strong>如何诊断</strong><strong>百日咳杆菌引起的百日咳？</strong><br>
    1、从卡他期或阵发初期所采集的鼻咽部标本中，培养出百日咳鲍特菌。传统来讲，细菌培养被视作实验室确诊的金标准，细菌培养是特异的，但不很敏感，且需要选择<br>
    性培养基。<br>
    2、血清学诊断是基于在配对的血清标本中，检测到PT 特异性抗体浓度的显著升高；应分别在卡他初期（急性期血清）和约一个月后（恢复期血清）采集血清标本。未接种疫苗个体的血清中抗体浓度高，表明近期感染。<br>
    &nbsp;<br>
    <strong>感染百日咳后体内抗体免疫保护水平是如何变化的？</strong><br>
    初次感染（急性），前5-10天内可检测到IgM，可持续6-12周。<br>
    发病后11天，可检测IgA，持续6-24周。<br>
    病情发生2-3周时可检测到IgG。<br>
    再次感染时，抗毒素IgG和IgA抗体会增加。<br>
    &nbsp;<br>
    在大龄儿童、青少年和成人中，由于其病程常常不典型，通常未被辨识为百日咳；<br>
    自然感染后，仅80-85%的患者出现抗 PT（百日咳鲍特菌唯一特异性抗原）抗体。</div>
