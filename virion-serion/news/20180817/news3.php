          <h3>关于破伤风，你真的了解吗？</h3>
          <!-- JiaThis Button BEGIN -->
          <div class="clearfix" style=" margin-top:10px; margin-bottom:10px; text-align:right;"> </div>
          <!-- JiaThis Button END -->
          <p>　</p><div style="text-align: center;">
    作者：温欣欣 周鹏</div>
<div>
    <strong>什么是破伤风？</strong><br>
    破伤风通常是致死性的感染性疾病，经割口或伤口由产毒的破伤风梭菌（Clostridium tetani）的孢子引起的急性感染性疾病。<br>
    &nbsp;<br>
    <strong>什么是</strong><strong>破伤风梭菌？</strong><br>
    破伤风梭菌是一种能形成芽孢的严格厌氧杆菌。芽孢在自然环境中广泛存在，尤其是在暖湿地区的土壤中，且可经人类和动物的肠道携带。<br>
    &nbsp;<br>
    <strong>破伤风梭菌的</strong><strong>致病因子</strong><br>
    破伤风梭菌产生的最重要的毒素是毒性极强的破伤风痉挛毒素。<br>
    &nbsp;<br>
    <strong>破伤风痉挛毒素的作用？</strong><br>
    这种毒素可阻断中枢神经系统中的抑制性神经递质，引发肌肉强直和痉挛，亦即破伤风的典型表现。<br>
    &nbsp;<br>
    &nbsp;<br>
    <strong>感染人群</strong><br>
    破伤风可在任何年龄发病，即便有了现代加强监护的条件，病例死亡率也非常高。<br>
    &nbsp;<br>
    <strong>感染破伤风梭菌的症状</strong><br>
    破伤风的潜伏期通常为3~21天（中位数7天，范围0~&gt;60天）。<br>
    在多数病例中，新生儿破伤风在出生后3~14天即可发病。<br>
    在超过80%的病例中，破伤风表现为全身性强直性疾病。<br>
    早期表现为面部肌肉痉挛（牙关紧闭和痉笑），继以背肌痉挛（角弓反张）和突发的全身性强直性癫痫（破伤风痉挛）。声门痉挛可诱发猝死。<br>
    在新生儿破伤风中，全身性痉挛出现前常表现为不能吮吸或接受喂食和过度哭闹。<br>
    破伤风的病死率在10%~70%之间，视治疗措施、患者的年龄和总体健康而异。<br>
    老年和婴幼儿患者如不住院和接受加强治疗，死亡率几乎可达100%。在具备最佳医疗条件的医院中，死亡率可降至10%~20%。</div>
