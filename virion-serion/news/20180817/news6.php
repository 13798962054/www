          <h3>白喉疫苗及免疫保护水平检测</h3>
          <!-- JiaThis Button BEGIN -->
          <div class="clearfix" style=" margin-top:10px; margin-bottom:10px; text-align:right;"> </div>
          <!-- JiaThis Button END -->
          <p>　</p><div style="text-align: center;">
    作者： 温欣欣 &nbsp;周鹏</div>
<div>
    白喉疫苗的成份是白喉类毒素，即经处理的白喉毒素，可以诱生具有保护作用的抗毒素。<br>
    &nbsp;<br>
    <strong>生产流程</strong><br>
    常用的生产流程是用液体培养基培养产毒的白喉棒状杆菌，对含外毒素的培养上清进行灭菌，用甲醛处理外毒素使其变成类毒素，然后用铝佐剂吸附，对于不含无细胞百日咳疫苗成份（aP）的多剂量瓶装疫苗，加入硫柳汞作为防腐剂<br>
    &nbsp;<br>
    <strong>白喉类毒素的产品</strong><br>
    破伤风类毒素（T）组成的联合疫苗（DT）。<br>
    与破伤风类毒素和百日咳疫苗组成的联合疫苗（DTP），其中的百日咳疫苗为百日咳全菌体（wP）或无细胞百日咳疫苗成份（aP）。<br>
    含白喉类毒素的疫苗应在＋4℃（2～8℃）保存。已冻结的疫苗不能使用。<br>
    &nbsp;<br>
    <strong>接种方式</strong><br>
    疫苗接种方式为只能肌肉注射。<br>
    &nbsp;<br>
    <strong>接种程序</strong>
    <div style="text-align: center;">
        <img alt="" src="/uploads/allimg/180813/1-1PQ31G2144L.png" style="width: 549px; height: 274px;"></div>
    &nbsp;<br>
    <strong>百白破疫苗</strong><strong>受种儿童需分别于3、4、5月龄和18月龄各接种1剂次，共4剂次。</strong><br>
    &nbsp;<br>
    基础免疫程序完成后平均保护时间为10年左右。<br>
    保护性免疫在接触产毒的白喉棒状杆菌后会得到加强。如不能通过此自然途径获得加强免疫，对婴儿期后及小学低年级儿童需进行白喉类毒素的加强免疫，以便维持保护性免疫力。<br>
    &nbsp;<br>
    &nbsp;<br>
