          <h3>卫计委明确：三级医院必备医疗设备和诊疗科目</h3>
          <!-- JiaThis Button BEGIN -->
          <div class="clearfix" style=" margin-top:10px; margin-bottom:10px; text-align:right;"> </div>
          <!-- JiaThis Button END -->
          <p>　</p><h2 class="rich_media_title" id="activity-name" style="margin: 0px 0px 14px; padding: 0px; font-weight: 400; font-size: 22px; line-height: 1.4; color: rgb(51, 51, 51); font-family: -apple-system-font, BlinkMacSystemFont, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei UI&quot;, &quot;Microsoft YaHei&quot;, Arial, sans-serif; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; widows: 1;">
    &nbsp;</h2>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    10月18日，国家卫计委办公厅公开发布《关于印发三级综合医院医疗服务能力指南（2016年版）的通知》（国卫办医函〔2016〕936号）。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    在征求意见两年之后，卫计委《三级综合医院医疗服务能力指南（2016 年版）》（以下简称《指南》）终于出炉，也为未来全国范围内三级综合医院的的样子划定了明确标尺。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    依据《指南》，三级综合医院应具有与三级医院服务功能相匹配，能够满足临床诊治疑难重症、以及开展医疗技术等服务需求的医疗设备。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    具体来说，三级综合医院的必备医疗设备包括：</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    1、全自动生化分析仪、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    2、全自动化学发光免疫分析仪、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    3、彩超（二维彩超、三维彩超）、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    4、计算机断层扫描（CT）（64排及以上）、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    5、数字X线（DR、CR）、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    6、磁共振成像（MRI）（1.5T 及以上）、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    7、数字减影血管造影（DSA）、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    8、数字胃肠透视机、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    9、乳腺X光机、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    10、胃肠X光机、</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    11、移动式Ｃ臂数字减影血管造影机等。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    此外，三级综合医院还应具有相适应的床位规模、 诊疗科目、结构合理的卫生技术人员，以满足三级医院服务功能、技术水平及管理要求。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    在床位规模上，外科床位数占医院实际开放床位数比例应≥30%。重症医学科（含所有专业ICU）的床位数占医院实际开放床位数比例应在2%-8%之间。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    而具体到诊疗科目，三级综合医院应当设置但不仅限于以下诊疗科目：</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    1、一级诊疗科目</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    应当设置的一级诊疗科目包括：预防保健科、内科、外科、妇产科、儿科、眼科、耳鼻咽喉科、口腔科、皮肤科、精神科、感染科、肿瘤科、急诊医学科、康复医学科、麻醉科、重症医学科、医学检验科、病理科、医学影像科、中医科。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    可以设置的一级诊疗科目：小儿外科。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    2、二级诊疗科目</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    内科应当包括：呼吸内科、消化内科、神经内科、心血管内科、血液内科、肾病学科、内分泌科、免疫学科。可提供与老年病科相对应的专科医疗服务。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    外科应当包括：普通外科、骨科、神经外科、泌尿外科、胸外科。可以有：心脏大血管外科、整形外科、烧伤科。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    妇产科应当包括：妇科、产科。可以有：计划生育科、生殖健康与不孕症科、优生学科、妇科内分泌专科。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    儿科应当提供与新生儿科相对应的专科医疗服务。可以有：小儿传染病科、小儿消化科、小儿呼吸科、小儿心脏病科、小儿肾病科、小儿血液科、小儿神经病科、小儿内分泌科、小儿遗传科、小儿免疫科。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    精神科应当包括：心身医学科、心理治疗科。可以有：老年精神科、精神康复科、儿童少年精神科、睡眠医学科。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    医学检验科应当包括：临床体液检验、血液检验，临床微生物学检验，临床生化检验，临床免疫、血清学检验，临床细胞分子遗传学检验。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    医学影像科应当提供与以下二级诊疗科目相对应的专业医疗服务：X线诊断、计算机断层扫描（CT）诊断、磁共振成像（MRI）诊断、核医学诊疗、超声诊断、心电诊断、脑电及脑血流图诊断、神经肌肉电图诊断、介入放射诊疗、放射治疗。</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp;</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    （备注：卫计委《三级综合医院医疗服务能力指南（2016 年版）》全文共有108页，针对各个一级、二级科室应具备的诊治手段和关键医疗技术均有详细介绍，感兴趣的微友可以直接点击本文后的“阅读原文”获取。）</p>
<p style="font-size: 17px; letter-spacing: 0.544px; text-align: justify; margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; box-sizing: border-box !important; word-wrap: break-word !important;">
    来源：国家卫计委</p>
