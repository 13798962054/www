          <h3>2018年维润中国合作伙伴技术培训研讨会顺利召开</h3>
          <!-- JiaThis Button BEGIN -->
          <div class="clearfix" style=" margin-top:10px; margin-bottom:10px; text-align:right;"> </div>
          <!-- JiaThis Button END -->
          <p>　</p><div style="text-align: center;">
    作者：王赫群</div>
&nbsp;<br>
&nbsp; &nbsp; &nbsp; &nbsp; <span style="font-size:14px;">2018年10月25-27日，维润赛润生物技术（深圳）有限公司在中国深圳成功举办中国区合作伙伴技术培训研讨会。这是维润赛润生物技术（深圳）有限公司举办的第三届技术研讨会。<br>
&nbsp; &nbsp; &nbsp; &nbsp;本次会议邀请了国内部分重要合作伙伴及潜力合作伙伴参会；公司总经理宁紫妍女士携团队主要成员与来自全国各地的合作伙伴共同参加了会议，会议同时邀请了深圳市中润医学技术研究院院长闵书亮博士参加。<br>
&nbsp; &nbsp; &nbsp; &nbsp; 26日会议伊始，宁紫妍总经理首先转达了总部德国维润赛润研发有限公司对各位合作伙伴的感谢和问候，祝贺本次会议圆满成功！宁总在致辞中，对各地合作伙伴到来表示热烈欢迎，对大家长期以来对维润赛润品牌和产品的支持与认同表示衷心感谢。宁总提出研讨会将按照小规模、高质量和多创新的形式进行，并对新经济形势和行业宏观经济政策新变化情况下，IVD企业将面对的新挑战和对策分享了自己的思考。宁总强调，维润赛润深圳公司将与各合作伙伴更加紧密的合作，相信未来会共同创造更多的市场价值。</span><br>
<br>
<div style="text-align: center;">
    <img alt="" src="/uploads/allimg/181031/1-1Q0311A212541.jpg" style="width: 850px; height: 275px;"></div>
&nbsp; &nbsp; &nbsp; &nbsp;<br>
<span style="font-size:14px;">&nbsp; &nbsp; &nbsp; &nbsp;26日研讨会分为两个半场在上下午分别进行，上午由公司副总经理朱峰、产品经理温欣欣等分享了共6个产品的深度研究报告，介绍了质控品相关的监管政策、市场推广过程的问题解答，IgA抗体检测的独特临床意义，疫苗接种后免疫保护水平定量检测，疱疹病毒相关性病检测，带状疱疹病毒的检测，细小病毒B19检测的新应用等。</span><br>
<div style="text-align: center;">
    <img alt="" src="/uploads/allimg/181031/1-1Q0311FA4313.png" style="width: 850px; height: 138px;"></div>
<br clear="ALL">
<span style="font-size:14px;">&nbsp; &nbsp; &nbsp; &nbsp; 下午由闵书亮博士对当前经济形势的新变化与新情况进行了深入剖析和讲解，并就新形势下行业出现的新挑战做的冷静观察与深刻思考进行了分享。闵书亮博士为时4个多小时的精彩报告引起会场上各位合作伙伴的热烈反响，与会的各位企业家纷纷发言，表达各自看法，表示对演讲内容有相同感受，深受触动，同时也从分享中得到了收获与启示。</span><br>
<div style="text-align: center;">
    <img alt="" src="/uploads/allimg/181031/1-1Q0311FGL20.jpg" style="width: 850px; height: 157px;"></div>
<br clear="ALL">
&nbsp; &nbsp; &nbsp; &nbsp; <span style="font-size:14px;">27日的技术培训实操部分在中南医学检验实验室拉开序幕。培训人员在实验室指导老师一对一的指导培训下，通过亲自动手操作产品的整套实验，更加清晰了维润赛润产品的操作流程和方法，在今后指导客户操作更多了些经验和技巧。</span>
<div style="text-align: center;">
    <img alt="" src="/uploads/allimg/181031/1-1Q0311FI6442.jpg" style="width: 850px; height: 115px;"></div>
&nbsp;<br clear="ALL">
<span style="font-size:14px;">&nbsp; &nbsp; &nbsp; &nbsp; 为期三天的技术培训研讨会，加强了维润赛润生物技术（深圳）有限公司与各地合作伙伴的沟通交流。各合作伙伴对维润赛润愿与大家共同前进、共享共赢的理念和做法表示衷心的感谢，纷纷表达了紧密合作的坚定信念。大家愉快地共同约定，明年，不见不散！</span>
