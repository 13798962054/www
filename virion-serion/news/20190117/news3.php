    <h3>猫寄生虫是如何影响我们的免疫系统的？</h3>
          <!-- JiaThis Button BEGIN -->
          <div class="clearfix" style=" margin-top:10px; margin-bottom:10px; text-align:right;"> </div>
          <!-- JiaThis Button END -->
          <p>　<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 2016年12月9日 讯 /生物谷BIOON/ --全球范围内50%的人群都受到&quot;猫寄生虫&quot;-刚地弓形虫的感染。在某些地区，这一感染率达到了95%。然而，除非亲自做相关的检查，否则难以发现自己受到了影响，但感染该寄生虫后却会产生一系列的精神异常症状，比如精神分裂或躁郁症等。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    研究者们最近发现了刚地弓形虫之所以能够逃离宿主免疫监察的分子机制。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 对于不知情的人来讲，刚地弓形虫仅仅是寄生在猫身上的一类能够引发弓形虫病的病原体。但其在免疫系统较弱的个体（例如孕妇，老人等）能够产生别的影响。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    当然，大部分医生都不会建议大家做专门的检测（除非万不得已），但一些研究发现其与被感染的宿主之间的一些异常行为之间存在密切的联系。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    来自捷克Charles大学的进化生物学家Jaroslav Flegr认为，弓形虫的感染能够影响我们在危险情形下的判断，因此，它与疟疾一样，也是杀伤力巨大的寄生虫类。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    过去的一些研究发现，受到弓形虫感染的小鼠在闻到猫的气味后不再害怕，而且它们的记忆力也受到了明显的损伤。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 因此，研究者们认为该寄生虫能够扰乱小鼠的大脑回路，而且他们正在研究该现象是否同样发生在人类身上。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 在众多关于弓形虫的研究中，至少有一点是肯定的，那就是它具备操控宿主免疫系统的能力。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 事实上，在地球上的某些地方，整个居民群体都受到了弓形虫的感染，这表明了它在逃脱免疫系统杀伤方面的能力。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 弓形虫一方面能够使宿主的免疫反应降到极低的水平，从而保证其生存；另一方面又能够使被感染的宿主免疫反应提高到较高水平，从而保证其身体健康，以作为一个巨大的&quot;弓形虫孵化器&quot;。如今，科学家们找到了弓形虫的感染策略。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 在正常情况下，细胞在识别弓形虫之后会激活p38蛋白的表达，以及入核，从而能够激活下游一系列炎性因子的表达并对弓形虫进行杀伤。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 而弓形虫能够主动地开启炎性因子的激活，并根据不同的需要进行程度调节。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 来自法国的研究者Mohamed-Ali Hakimi发现弓形虫能够分泌一类叫做GRA24的蛋白，该蛋白能够直接激活p38，而这一现象的发生要远远早于宿主自身免疫系统的反应。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 最近，研究者们发现GRA24能够以更高的亲和力与p33结合，从而阻断了宿主的信号传递。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 同时，该蛋白能够阻止炎性反应的关闭，这也就是为什么该疾病对于免疫系统较弱的群体来讲十分危险。</p>
<p style="margin: 0px; padding: 0px; max-width: 100%; clear: both; min-height: 1em; font-variant-numeric: normal; font-variant-east-asian: normal; letter-spacing: 0.544px; text-align: justify; widows: 1; color: rgb(62, 62, 62); font-family: -apple-system-font, &quot;Helvetica Neue&quot;, &quot;PingFang SC&quot;, &quot;Hiragino Sans GB&quot;, &quot;Microsoft YaHei&quot;, sans-serif; font-size: 16px; line-height: 20.48px; box-sizing: border-box !important; word-wrap: break-word !important;">
    &nbsp; &nbsp; &nbsp; 相关结果发表在《structure》杂志上。（生物谷Bioon.com）</p>
