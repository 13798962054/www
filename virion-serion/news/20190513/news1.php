<p class="MsoNormal" style="text-indent:23.0pt;mso-char-indent-count:2.0;
line-height:150%">
    <span style="font-size:12.0pt;mso-bidi-font-size:10.5pt;
line-height:150%">近几年来，由真菌（主要是念珠菌、隐球菌、曲霉菌和孢子菌等）感染引发的疾病越来越引起人们的重视。据统计，目前全球有近<span lang="EN-US">3</span>亿人患有不同程度真菌引起的相关疾病，全球每年因真菌感染及其并发症导致的死亡人数或超过<span lang="EN-US">160</span>万，与结核病导致的死亡人数相近。而英国政府资助的一项研究表明，如果不制定政策来减缓包括真菌等引起的耐药性的上升，到<span lang="EN-US">2050</span>年，全球将有<span lang="EN-US">1000</span>万人死于耐药性，超过预计将因癌症死亡的<span lang="EN-US">800</span>万人。因此，真菌对目前已知的抗菌药物耐药性的增加，是当今社会要面临的一项重大问题；而像耳念珠菌这样具有多重耐药性念珠菌&mdash;&mdash;超级细菌的出现，更是全球健康要面对的一项严重挑战。<span lang="EN-US"><span style="mso-spacerun:yes">&nbsp;&nbsp; </span></span></span></p>
<p class="MsoNormal" style="text-indent:23.1pt;mso-char-indent-count:2.0;
line-height:150%">
