<p align="left" class="MsoNormal" style="text-align:left">
    <b style="mso-bidi-font-weight:
normal"><span style="font-size: 12pt; font-family: &quot;微软雅黑&quot;, sans-serif;">流行病学特征： </span></b><b style="mso-bidi-font-weight:
