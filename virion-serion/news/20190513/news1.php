        <span style="font-size:12.0pt;line-height:150%;
color:#333333;"><span style="font-size:12.0pt;line-height:150%;color:black;">2. 基于基因组测序的方法。</span></span></p>
    <p align="left" class="MsoNormal" style="text-align:left;text-indent:24.0pt;
