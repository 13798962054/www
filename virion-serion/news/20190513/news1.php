        <span style="font-size:12.0pt;line-height:150%;color:#333333;
;mso-bidi-font-weight:bold">美国<span lang="EN-US">CDC</span>认为，耳念珠菌会给全球健康都带来严重威胁。基于以下三点要密切关注这一真菌：</span></p>
    <p align="left" class="MsoNormal" style="text-align:left;line-height:150%">
