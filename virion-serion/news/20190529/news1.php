&nbsp;<br />
&nbsp;<br />
疑问及解答：<br />
疑问一：我打过水痘疫苗，还用接种带状疱疹疫苗么？<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 答：水痘疫苗只能预防水痘，不能预防带状疱疹和带状疱疹后神经痛，是需要接种带状疱疹疫苗的。<br />
&nbsp;<br />
疑问二：我没到50岁，还正直大好时光，也需要接种么？<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 答：水痘带状疱疹病毒感染，可以表现为两种临床症状：</span><br />
<ol>
    <li>
        <span style="font-size:16px;">水痘，好发于儿童期；</span></li>
    <li>
        <span style="font-size:16px;">带状疱疹，好发于中老年人。</span></li>
</ol>
<span style="font-size:16px;">如果您是水痘易感人群（也就是说血清学阴性），那么就不用接种带状疱疹疫苗，但需要接种水痘疫苗以预防水痘；如果你以前有得过水痘，也有必要复查下体内的抗体保护水平，在处于水痘高风险区域时，是否有足够的免疫抵抗力，预防水痘感染也是相当重要的。当然，后期可以进行预防带状疱疹疫苗。<br />
疑问三：我想接种带状疱疹疫苗，可是我不知道以前是否得过水痘，且水痘呈血清学阴性又是什么？？<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 答：如果不知道自己是否得过水痘，很简单，做个检测就知道了，使用德国维润赛润水痘带状疱疹病毒抗体诊断试剂，检测下IgG抗体，如果结果显示＞100mIU/mL，则表明既往有过感染，或者有接种过水痘疫苗。若是结果显示＜50mIU/mL，证明是血清学阴性，这时，您需要注意了，水痘的易感人群，需要接种水痘疫苗进行预防。<br />
&nbsp;<br />
疑问四：哪种试剂检测水痘血清学阴性最可靠？<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 答：<strong>德国维润赛润水痘带状疱疹抗体检测试剂</strong>。据最新资料显示，目前在NMPA有注册证的水痘带状疱疹病毒检测试剂生产厂家不超过5家，IgA/IgG/IgM三个品种皆有并且能够定量检测IgG抗体的厂家仅有德国维润赛润一家！</span><br />
<ol>
    <li>
        <span style="font-size:16px;"><strong>疫苗的有效成分均为糖蛋白抗原，而德国维润赛润恰恰使用的正是纯化VZV糖蛋白抗原，</strong>具有高灵敏度,高特异性对疫苗接种后产生的抗体检测完全匹配。</span></li>
    <li align="left">
        <span style="font-size:16px;">是进口独家产品。</span></li>
    <li>
        <span style="font-size:16px;">相对国内的诊断试剂而言，<strong>德国维润赛润的水痘带状疱疹抗体检测试剂是唯一可以定量的产品</strong>（国际公认4PL方法进行精确的定量（IgG））。</span></li>
    <li align="left">
        <span style="font-size:16px;"><strong>有效期长达18个月</strong>，比国产产品有效期长半年。</span></li>
    <li>
        <span style="font-size:16px;">依据WHO标准，可溯源性。目前国产产品无法做到这一点。</span></li>
    <li>
        <span style="font-size:16px;"><strong>项目齐全</strong>，具有水痘带状疱症病毒IgA/IgG/IgM 三种产品，尤其是IgA ，IgA抗体在患者潜伏感染再激活期间是明显增加，并且经常与带状疱疹发作相关。检测是最敏感的，并且很多研究表明，仅仅以IgG和IgM抗体作为带状疱疹的诊断价值有限，尚不能作为带状疱疹的特异性诊断依据，而IgA在带状疱疹组合正常对照比较中有统计学差异。国内的其他厂家只有IgM或者IgG项目，检测项目既不齐全，也不能做到定量检测。</span></li>
</ol>
