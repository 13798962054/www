<p class="MsoNormal" style="text-indent: 21pt;">
    <span style="font-size:16px;"><span style="font-family: &quot;微软雅黑&quot;, sans-serif; color: rgb(46, 48, 51); background: white none repeat scroll 0% 0%;">重组疫苗应为乳白色、无色至淡棕色液体。在给药前检查颗粒物质和变色情况。如果存在上述任何一种情况，就不应接种疫苗。<br />
    &nbsp;<br />
    重组后立即使用SHINGRIX，或将SHINGRIX置于2&deg;C至8&deg;C(36&deg;F至46&deg;F)之间冷藏，6小时内使用。如在6小时内未使用重组疫苗，请弃用。<br />
    &nbsp;<br />
    <strong>SHINGRIX</strong><strong>不应用于:</strong></span></span></p>
<ol>
    <li>
        <span style="font-size:16px;">不要给任何有严重过敏反应史(如过敏反应)的人使用SHINGRIX疫苗的任何成分或在之前使用SHINGRIX过敏(见说明)。</span></li>
    <li align="left">
        <span style="font-size:16px;"><strong>已知对水痘呈血清阴性的人。</strong></span></li>
    <li align="left">
        <span style="font-size:16px;">带状疱疹急性发作的人，SHINGRIX不能治疗带状疱疹或带状疱疹后神经痛(PHN)。任何疫苗的一般指导方针都是等到疾病的急性期结束，症状减轻后再接种。</span></li>
    <li align="left">
        <span style="font-size:16px;">目前正在怀孕或哺乳。怀孕或哺乳的妇女应该等待SHINGRIX。</span></li>
    <li align="left">
        <span style="font-size:16px;">接种前24小时接受特定的抗病毒药物(阿昔洛韦、泛昔洛韦或伐昔洛韦)(接种后14天避免使用这些抗病毒药物)</span></li>
</ol>
<span style="font-size:16px;">&nbsp;<br />
<strong>不良反应：</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 在50岁及以上的受试者中，局部不良反应为疼痛(78.0%)、发红(38.1%)和肿胀(25.9%)。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 在50岁及以上的受试者中征集的一般不良反应为肌痛(44.7%)、疲劳(44.5%)、头痛(37.7%)、发抖(26.8%)、发烧(20.5%)和胃肠道症状(17.3%)。<br />
&nbsp;<br />
<strong>SHINGRIX</strong><strong>的效果：</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 两剂带状疱疹可有效预防带状疱疹和带状疱疹后神经痛(PHN，带状疱疹最常见的并发症)</span><br />
<ol>
    <li align="left">
        <span style="font-size:16px;">在50岁至69岁的成年人中，两剂SHINGRIX预防带状疱疹的有效率为97%</span></li>
    <li align="left">
        <span style="font-size:16px;">在70岁及以上的成年人中，SHINGRIX的有效率为91%</span></li>
    <li align="left">
        <span style="font-size:16px;">在50至69岁的成年人中，两剂的SHINGRIX预防PHN的效果为91%</span></li>
    <li align="left">
        <span style="font-size:16px;">在70岁及以上的成年人中，SHINGRIX的有效率为89%。</span></li>
</ol>
<span style="font-size:16px;">在接种疫苗后的四年中，70岁及以上人群的SHINGRIX保护率仍然很高(超过85%)。随着年龄的增长，患带状疱疹和PHN的风险也会增加，因此，对带状疱疹有很强的预防是很重要的。<br />
参考来源</span><br />
